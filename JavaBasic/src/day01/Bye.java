package day01;

public class Bye {

		public static void main(String[] args) {
			
			//main이라 적고 ctrl+space를 누르면 실행함수가 만들어집니다.
			/*
			 * ctrl+s 저장
			 * ctrl+F11 실행
			 * sysout이라 적고 ctrl+space 출력문 자동완성이 만들어집니다.
			 */
			System.out.println("출력문 자동완성");
				
			/*
			 * \n - 줄바꿈
			 * \t - 8칸 공백을 잡고 정렬
			 */
			System.out.printf("제 이름은 %s 입니다\n", "coding404");
			System.out.printf("원주율 %.2f 입니다\n", 3.14);

        } 
}	